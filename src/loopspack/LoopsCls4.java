package loopspack;

public class LoopsCls4 {

    public static void main(String[] args) {

        String netice = "";


        for(int i = 1;i<=4;i++){

            for(int j = 1;j<=4;j++){

               netice += "*";

            }

           netice+="\n";

        }



        System.out.println(netice);

    }


}
